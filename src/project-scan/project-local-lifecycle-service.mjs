import fs from "node:fs";
import path from "node:path";
import { copyDir, ensureDir, readJson, removeDirIfExists, writeJson } from "../shared/fs.mjs";
import { listProjectSkillSummaries } from "./project-local-skills.mjs";
import { planProjectLocalSkillPromotions } from "./project-local-promotion-planner.mjs";

export function createProjectLocalLifecycleService({
  projectRoot,
  getAnalysisPaths,
  ensureOverlayRoot,
  loadProjectPackageJson,
  promotionTraceService
}) {
  function listProjectLocalSkills() {
    const paths = getAnalysisPaths();
    ensureOverlayRoot(paths);
    const autoGeneratedSkills = fs.existsSync(paths.autoGeneratedRoot)
      ? listProjectSkillSummaries(paths.autoGeneratedRoot, "auto-generated")
      : [];
    const manualSkills = fs.existsSync(paths.manualRoot)
      ? listProjectSkillSummaries(paths.manualRoot, "manual")
      : [];

    return {
      projectName: loadProjectPackageJson().name || path.basename(projectRoot),
      manualSkills,
      autoGeneratedSkills
    };
  }

  function planProjectLocalPromotions({ skillName = "", criteria = {} } = {}) {
    const paths = getAnalysisPaths();
    const result = listProjectLocalSkills();

    return planProjectLocalSkillPromotions({
      projectName: result.projectName,
      autoGeneratedSkills: result.autoGeneratedSkills,
      manualSkills: result.manualSkills,
      manualRoot: paths.manualRoot,
      skillName,
      criteria
    });
  }

  function promoteProjectLocalSkill({ skillName, force = false } = {}) {
    if (!skillName) throw new Error("promote-project-local-skill requires a skill name.");

    const paths = getAnalysisPaths();
    const sourceRoot = path.join(paths.autoGeneratedRoot, skillName);
    const targetRoot = path.join(paths.manualRoot, skillName);
    if (!fs.existsSync(sourceRoot)) throw new Error(`Auto-generated skill not found: ${skillName}`);
    if (fs.existsSync(targetRoot) && !force) throw new Error(`Manual project-local skill already exists: ${skillName}. Use --force to overwrite.`);

    ensureOverlayRoot(paths);
    ensureDir(paths.manualRoot);
    if (fs.existsSync(targetRoot)) removeDirIfExists(targetRoot);
    copyDir(sourceRoot, targetRoot);

    const metaPath = path.join(targetRoot, "skill.meta.json");
    const meta = fs.existsSync(metaPath) ? readJson(metaPath) : { name: skillName };
    const promotedAt = new Date().toISOString();
    writeJson(metaPath, {
      ...meta,
      status: "active",
      source: "manual-promoted",
      promotedAt,
      promotedFrom: `auto-generated/${skillName}`
    });

    promotionTraceService?.recordRelation({
      relationType: "project-skill->manual-project-skill",
      source: {
        type: "project-skill",
        id: skillName,
        path: sourceRoot
      },
      target: {
        type: "manual-project-skill",
        id: skillName,
        path: targetRoot
      },
      metadata: {
        promotedAt,
        promotedFrom: `auto-generated/${skillName}`,
        patternId: meta.patternId || "",
        projectName: loadProjectPackageJson().name || path.basename(projectRoot)
      },
      recordedAt: promotedAt
    });

    return {
      skillName,
      promotedAt,
      sourceRoot,
      targetRoot
    };
  }

  return {
    listProjectLocalSkills,
    planProjectLocalPromotions,
    promoteProjectLocalSkill
  };
}
