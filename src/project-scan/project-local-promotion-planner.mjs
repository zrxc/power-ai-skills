import fs from "node:fs";
import path from "node:path";
import { readJson } from "../shared/fs.mjs";
import { sortProjectSkillNames } from "./project-local-skills.mjs";

export const DEFAULT_PROJECT_LOCAL_PROMOTION_CRITERIA = Object.freeze({
  requiredReviewDecision: "generate",
  minimumFrequency: 2,
  minimumReuseScore: 70,
  minimumPurityScore: 70,
  allowedConfidence: ["high", "medium"]
});

function readSkillMeta(skillRoot) {
  const metaPath = path.join(skillRoot, "skill.meta.json");
  return fs.existsSync(metaPath) ? readJson(metaPath) : {};
}

function toNumericScore(value) {
  return Number.isFinite(Number(value)) ? Number(value) : 0;
}

function normalizeConfidence(value) {
  return String(value || "").trim().toLowerCase();
}

function buildCriteria(criteria) {
  const allowedConfidence = Array.isArray(criteria.allowedConfidence) && criteria.allowedConfidence.length > 0
    ? criteria.allowedConfidence.map((item) => normalizeConfidence(item)).filter(Boolean)
    : DEFAULT_PROJECT_LOCAL_PROMOTION_CRITERIA.allowedConfidence;

  return {
    requiredReviewDecision: criteria.requiredReviewDecision || DEFAULT_PROJECT_LOCAL_PROMOTION_CRITERIA.requiredReviewDecision,
    minimumFrequency: Number.isFinite(Number(criteria.minimumFrequency))
      ? Number(criteria.minimumFrequency)
      : DEFAULT_PROJECT_LOCAL_PROMOTION_CRITERIA.minimumFrequency,
    minimumReuseScore: Number.isFinite(Number(criteria.minimumReuseScore))
      ? Number(criteria.minimumReuseScore)
      : DEFAULT_PROJECT_LOCAL_PROMOTION_CRITERIA.minimumReuseScore,
    minimumPurityScore: Number.isFinite(Number(criteria.minimumPurityScore))
      ? Number(criteria.minimumPurityScore)
      : DEFAULT_PROJECT_LOCAL_PROMOTION_CRITERIA.minimumPurityScore,
    allowedConfidence
  };
}

function buildBlocker(code, message) {
  return { code, message };
}

function buildCandidatePlan({ projectName, skillSummary, manualSkillMap, manualRoot, criteria }) {
  const meta = readSkillMeta(skillSummary.path);
  const manualSkill = manualSkillMap.get(skillSummary.name) || null;
  const confidence = normalizeConfidence(meta.confidence);
  const blockers = [];

  if (manualSkill) {
    blockers.push(buildBlocker(
      "manual-skill-already-exists",
      `Manual project-local skill already exists at ${manualSkill.path}.`
    ));
  }

  if ((meta.reviewDecision || "") !== criteria.requiredReviewDecision) {
    blockers.push(buildBlocker(
      "review-decision-not-generate",
      `reviewDecision must be ${criteria.requiredReviewDecision}, received ${meta.reviewDecision || "empty"}.`
    ));
  }

  if (toNumericScore(meta.frequency) < criteria.minimumFrequency) {
    blockers.push(buildBlocker(
      "frequency-below-threshold",
      `frequency ${toNumericScore(meta.frequency)} is below ${criteria.minimumFrequency}.`
    ));
  }

  if (toNumericScore(meta.reuseScore) < criteria.minimumReuseScore) {
    blockers.push(buildBlocker(
      "reuse-score-below-threshold",
      `reuseScore ${toNumericScore(meta.reuseScore)} is below ${criteria.minimumReuseScore}.`
    ));
  }

  if (toNumericScore(meta.purityScore) < criteria.minimumPurityScore) {
    blockers.push(buildBlocker(
      "purity-score-below-threshold",
      `purityScore ${toNumericScore(meta.purityScore)} is below ${criteria.minimumPurityScore}.`
    ));
  }

  if (!criteria.allowedConfidence.includes(confidence)) {
    blockers.push(buildBlocker(
      "confidence-not-allowed",
      `confidence ${meta.confidence || "empty"} is not in ${criteria.allowedConfidence.join(", ")}.`
    ));
  }

  const targetRoot = path.join(manualRoot, skillSummary.name);

  return {
    skillName: skillSummary.name,
    status: blockers.length > 0 ? "blocked" : "eligible",
    sourceType: skillSummary.sourceType,
    sourceRoot: skillSummary.path,
    targetRoot,
    projectName,
    blockers,
    evidence: {
      reviewDecision: meta.reviewDecision || "",
      reviewReasons: meta.reviewReasons || [],
      confidence: meta.confidence || "",
      frequency: toNumericScore(meta.frequency),
      reuseScore: toNumericScore(meta.reuseScore),
      purityScore: toNumericScore(meta.purityScore),
      generatedAt: meta.generatedAt || "",
      existingManualSkill: manualSkill
        ? {
            path: manualSkill.path,
            updatedAt: manualSkill.updatedAt || "",
            source: manualSkill.source || ""
          }
        : null
    },
    manualConfirmation: {
      mode: "command",
      command: `npx power-ai-skills promote-project-local-skill ${skillSummary.name}`
    }
  };
}

export function planProjectLocalSkillPromotions({
  projectName,
  autoGeneratedSkills,
  manualSkills,
  manualRoot,
  skillName = "",
  criteria = {}
}) {
  const normalizedCriteria = buildCriteria(criteria);
  const requestedSkillName = String(skillName || "").trim();
  const manualSkillMap = new Map((manualSkills || []).map((item) => [item.name, item]));
  const selectedSkills = requestedSkillName
    ? (autoGeneratedSkills || []).filter((item) => item.name === requestedSkillName)
    : [...(autoGeneratedSkills || [])];

  if (requestedSkillName && selectedSkills.length === 0) {
    throw new Error(`Auto-generated skill not found: ${requestedSkillName}`);
  }

  const candidates = sortProjectSkillNames(selectedSkills.map((item) => item.name)).map((name) => {
    const skillSummary = selectedSkills.find((item) => item.name === name);
    return buildCandidatePlan({
      projectName,
      skillSummary,
      manualSkillMap,
      manualRoot,
      criteria: normalizedCriteria
    });
  });

  const eligibleCount = candidates.filter((item) => item.status === "eligible").length;
  const blockedCount = candidates.length - eligibleCount;

  return {
    projectName,
    requestedSkillName,
    criteria: normalizedCriteria,
    summary: {
      autoGeneratedSkillCount: autoGeneratedSkills?.length || 0,
      manualSkillCount: manualSkills?.length || 0,
      evaluatedSkillCount: candidates.length,
      eligibleCount,
      blockedCount
    },
    manualConfirmation: {
      mode: "per-skill-command",
      description: "Review each eligible draft and promote it with the existing manual command boundary.",
      commandTemplate: "npx power-ai-skills promote-project-local-skill <skill-name>"
    },
    candidates
  };
}
