function renderScoreLine(label, value) {
  return `- ${label}: ${value}`;
}

export function buildProjectSkillMarkdown(patternDefinition, pattern, review, generatedAt) {
  const templateReference = "references/templates.md";
  const baseSkillPath = `../../../ui/${patternDefinition.baseSkill}/SKILL.md`;
  const featureLines = (pattern.features || []).map((feature) => `- \`${feature}\``).join("\n") || "- 无稳定特征";
  const fileLines = (pattern.sampleFiles || []).map((filePath) => `- \`${filePath}\``).join("\n") || "- 无样例文件";
  const componentLines = [
    pattern.componentStack?.page ? `- 页面容器：\`${pattern.componentStack.page}\`` : "",
    pattern.componentStack?.tree ? `- 树组件：\`${pattern.componentStack.tree}\`` : "",
    pattern.componentStack?.table ? `- 表格主体：\`${pattern.componentStack.table}\`` : "",
    pattern.componentStack?.dialog ? `- 弹窗：\`${pattern.componentStack.dialog}\`` : ""
  ].filter(Boolean).join("\n") || "- 暂未识别稳定组件组合";
  const subpatternLines = (pattern.subpatterns || []).map((item) => `- \`${item.name}\` x ${item.count}`).join("\n") || "- 无";
  const interactionLines = (pattern.interactionTraits || []).map((item) => `- \`${item}\``).join("\n") || "- 无";
  const flowLines = (pattern.dataFlowTraits || []).map((item) => `- \`${item}\``).join("\n") || "- 无";

  return `---
name: "${patternDefinition.skillName}"
description: "${patternDefinition.description}"
---

# ${patternDefinition.displayName}

本技能由 \`power-ai-skills scan-project\` 在 ${generatedAt} 自动生成，用于补齐当前项目已经存在的页面实现习惯。

## 开始实现前先读取

- 企业公共主 skill：\`${baseSkillPath}\`
- 项目扫描摘要：\`${templateReference}\`

## 当前项目识别结果

${componentLines}

子模式分布：
${subpatternLines}

交互特征：
${interactionLines}

数据流特征：
${flowLines}

命中特征：
${featureLines}

代表样例：
${fileLines}

## 质量分

${renderScoreLine("dominantSubpattern", `\`${pattern.dominantSubpattern}\``)}
${renderScoreLine("confidence", `\`${pattern.confidence}\``)}
${renderScoreLine("structuralScore", pattern.structuralScore)}
${renderScoreLine("purityScore", pattern.purityScore)}
${renderScoreLine("reuseScore", pattern.reuseScore)}
${renderScoreLine("reviewDecision", `\`${review.decision}\``)}

## 实现清单

1. 先复用企业公共 skill，不要在这里重写通用规则。
2. 读取 \`${templateReference}\`，确认当前项目常见的页面骨架、命名和组件组合。
3. 只补充项目差异，如字段组织、事件命名、数据回刷顺序和组件搭配。
4. 如果项目代码与公共 skill 冲突，优先遵循当前项目已经存在的高频实现模式。
5. 如果需要把这份草案沉淀为人工维护规则，执行 \`power-ai-skills promote-project-local-skill ${patternDefinition.skillName}\`。

## 注意事项

- 这是自动生成草案，不等同于人工确认后的正式规则。
- 生成门槛基于 1.0.5 的频次、置信度、纯度分和复用分规则。
- 复核原因详见 \`.power-ai/analysis/pattern-review.json\` 和 \`.power-ai/reports/project-scan-summary.md\`。
`;
}

export function buildTemplatesReference(patternDefinition, pattern, review) {
  const featureLines = (pattern.features || []).map((feature) => `- \`${feature}\``).join("\n") || "- 无";
  const fileLines = (pattern.sampleFiles || []).map((filePath) => `- \`${filePath}\``).join("\n") || "- 无";
  const reasonLines = (review.reasons || []).map((reason) => `- ${reason}`).join("\n") || "- 无";
  const componentLines = [
    pattern.componentStack?.page ? `- 页面容器：\`${pattern.componentStack.page}\`` : "",
    pattern.componentStack?.tree ? `- 树组件：\`${pattern.componentStack.tree}\`` : "",
    pattern.componentStack?.table ? `- 表格主体：\`${pattern.componentStack.table}\`` : "",
    pattern.componentStack?.dialog ? `- 弹窗：\`${pattern.componentStack.dialog}\`` : ""
  ].filter(Boolean).join("\n") || "- 未识别稳定组件组合";
  const interactionLines = (pattern.interactionTraits || []).map((item) => `- \`${item}\``).join("\n") || "- 无";
  const flowLines = (pattern.dataFlowTraits || []).map((item) => `- \`${item}\``).join("\n") || "- 无";
  const subpatternLines = (pattern.subpatterns || []).map((item) => `- \`${item.name}\` x ${item.count}`).join("\n") || "- 无";

  return `# ${patternDefinition.displayName} 项目扫描摘要

## 基础信息

- 主 skill：\`${patternDefinition.baseSkill}\`
- 识别频次：${pattern.frequency}
- 置信度：\`${pattern.confidence}\`
- 平均得分：${pattern.averageScore}
- 主子模式：\`${pattern.dominantSubpattern}\`
- structuralScore：${pattern.structuralScore}
- purityScore：${pattern.purityScore}
- reuseScore：${pattern.reuseScore}

## 常见组件组合

${componentLines}

## 子模式分布

${subpatternLines}

## 交互特征

${interactionLines}

## 数据流特征

${flowLines}

## 命中特征

${featureLines}

## 生成原因

${reasonLines}

## 命中样例文件

${fileLines}
`;
}

export function buildAutoGeneratedReadme({ projectName, generatedSkillNames, patternReview }) {
  const generatedList = generatedSkillNames.length > 0
    ? generatedSkillNames.map((skillName) => `- \`${skillName}\``).join("\n")
    : "- 当前没有 pattern 达到自动生成门槛";

  return `# Auto Generated Project Local Skills

- project: \`${projectName}\`
- generated skills: ${generatedSkillNames.length}
- review file: \`.power-ai/analysis/pattern-review.json\`
- diff file: \`.power-ai/analysis/pattern-diff.json\`

## Generated

${generatedList}

## Pending Review

- 继续查看 \`.power-ai/reports/project-scan-summary.md\`，确认未自动生成的 pattern 是否需要人工沉淀。
- 如需按最新扫描结果增量更新草案，执行 \`npx power-ai-skills generate-project-local-skills\`。
- 如需强制全量重建 auto-generated 草案，执行 \`npx power-ai-skills generate-project-local-skills --regenerate-project-local\`。
- 如需将某个草案晋升为人工维护版本，执行 \`npx power-ai-skills promote-project-local-skill <skill-name>\`。

## Decisions

${patternReview.patterns.map((pattern) => `- \`${pattern.type}\`: ${pattern.decision}`).join("\n")}
`;
}
