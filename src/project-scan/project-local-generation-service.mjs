import { removeDirIfExists } from "../shared/fs.mjs";
import { patternDefinitions } from "./pattern-definitions.mjs";
import {
  buildAutoGeneratedReadme,
  buildProjectSkillMarkdown,
  buildTemplatesReference
} from "./project-local-renderers.mjs";
import { writeGeneratedProjectLocalSkills } from "./project-local-skills.mjs";

export function createProjectLocalGenerationService({
  getAnalysisPaths,
  loadAnalysisArtifacts,
  ensureOverlayRoot
}) {
  function generateProjectLocalSkills({ regenerate = false } = {}) {
    const paths = getAnalysisPaths();
    const analysisArtifacts = loadAnalysisArtifacts();
    ensureOverlayRoot(paths);
    if (regenerate) removeDirIfExists(paths.autoGeneratedRoot);

    const generationDetails = writeGeneratedProjectLocalSkills({
      paths,
      analysisArtifacts,
      patternDefinitions,
      renderSkillMarkdown: buildProjectSkillMarkdown,
      renderTemplatesReference: buildTemplatesReference,
      renderAutoGeneratedReadme: buildAutoGeneratedReadme
    });

    return {
      generatedAt: analysisArtifacts.patterns.generatedAt,
      projectName: analysisArtifacts.projectProfile.projectName,
      ...generationDetails,
      skipped: generationDetails.createdSkillNames.length === 0
        && generationDetails.updatedSkillNames.length === 0
        && generationDetails.removedSkillNames.length === 0
        && !generationDetails.readmeUpdated,
      reason: generationDetails.createdSkillNames.length === 0
        && generationDetails.updatedSkillNames.length === 0
        && generationDetails.removedSkillNames.length === 0
        && !generationDetails.readmeUpdated
        ? "auto-generated skills already up to date"
        : "",
      regenerated: regenerate,
      patternReview: analysisArtifacts.patternReview
    };
  }

  return {
    generateProjectLocalSkills
  };
}
