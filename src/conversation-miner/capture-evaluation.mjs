function normalizePatternList(values = []) {
  return (values || [])
    .map((item) => String(item || "").trim().toLowerCase())
    .filter(Boolean);
}

function matchesGeneratedFilePattern(generatedFiles = [], patterns = []) {
  const normalizedFiles = (generatedFiles || []).map((item) => String(item || "").trim().replace(/\\/g, "/").toLowerCase()).filter(Boolean);
  const normalizedPatterns = normalizePatternList(patterns);
  return normalizedPatterns.some((pattern) => normalizedFiles.some((filePath) => filePath.includes(pattern)));
}

export function createCaptureEvaluationService({ context, projectRoot, ensureConversationRoots }) {
  function loadConversationRecords({ from = "", to = "" } = {}) {
    const paths = ensureConversationRoots();
    const conversationFiles = fs.existsSync(paths.conversationsRoot)
      ? fs.readdirSync(paths.conversationsRoot).filter((fileName) => fileName.endsWith(".json")).sort((left, right) => left.localeCompare(right, "zh-CN"))
      : [];

    const records = [];
    for (const fileName of conversationFiles) {
      const date = fileName.replace(/\.json$/i, "");
      if (from && date < from) continue;
      if (to && date > to) continue;
      const payload = readJson(path.join(paths.conversationsRoot, fileName));
      for (const record of payload.records || []) records.push({
        ...record,
        fingerprint: record.fingerprint || createRecordFingerprint(record)
      });
    }

    return {
      paths,
      records: records.sort((left, right) => String(left.timestamp).localeCompare(String(right.timestamp), "zh-CN") || String(left.id).localeCompare(String(right.id), "zh-CN")),
      fileCount: conversationFiles.filter((fileName) => {
        const date = fileName.replace(/\.json$/i, "");
        return (!from || date >= from) && (!to || date <= to);
      }).length
    };
  }

  function loadProjectLocalSkillCoverage(paths = ensureConversationRoots()) {
    const skillRoots = [paths.autoGeneratedRoot, paths.manualRoot].filter((rootPath) => fs.existsSync(rootPath));
    const metas = [];
    for (const rootPath of skillRoots) {
      for (const entry of fs.readdirSync(rootPath, { withFileTypes: true })) {
        if (!entry.isDirectory()) continue;
        const metaPath = path.join(rootPath, entry.name, "skill.meta.json");
        if (!fs.existsSync(metaPath)) continue;
        metas.push(createCoverageSnapshot(readJson(metaPath)));
      }
    }
    return metas;
  }

  function evaluateCoverage(record, coverageMetas) {
    const recordOps = normalizeOperations(record.entities?.operations || []);
    const recordCustomizations = toUniqueSortedList(record.customizations || []);
    const recordMainObject = String(record.entities?.mainObject || "").trim();
    const recordTreeObject = String(record.entities?.treeObject || "").trim();

    for (const meta of coverageMetas) {
      if (!meta.sceneType || meta.sceneType !== normalizeSceneType(record.sceneType)) continue;

      if (meta.source === "conversation-miner") {
        const missingOps = recordOps.filter((operation) => !meta.operations.includes(operation));
        const missingCustomizations = recordCustomizations.filter((item) => !meta.customizations.includes(item));
        const missingMainObject = recordMainObject && meta.mainObjects.length > 0 && !meta.mainObjects.includes(recordMainObject);
        const missingTreeObject = recordTreeObject && meta.treeObjects.length > 0 && !meta.treeObjects.includes(recordTreeObject);
        if (missingOps.length === 0 && missingCustomizations.length === 0 && !missingMainObject && !missingTreeObject) {
          return { covered: true, skillName: meta.name };
        }
        continue;
      }

      if (recordCustomizations.length === 0 && recordOps.length <= 1) {
        return { covered: true, skillName: meta.name };
      }
    }

    return { covered: false, skillName: "" };
  }

  function evaluateRecordCandidate({ record, config, existingFingerprints, coverageMetas, batchFingerprints }) {
    const reasons = [];
    const sceneType = normalizeSceneType(record.sceneType);
    const isKnownScene = sceneDefinitions.has(sceneType);
    const hasGeneratedFiles = (record.generatedFiles || []).length > 0;
    const hasSkillSignals = (record.skillsUsed || []).length > 0;
    const hasCustomizations = (record.customizations || []).length > 0;
    const hasOperations = (record.entities?.operations || []).length > 0;
    const hasEntitySignals = Boolean(record.entities?.mainObject || record.entities?.treeObject);
    const isProjectRelated = isKnownScene || hasGeneratedFiles || hasSkillSignals || hasCustomizations || hasOperations || hasEntitySignals;
    const safetyPolicy = config.captureSafetyPolicy || {
      admission: {
        allowedSceneTypes: [],
        blockedSceneTypes: [],
        reviewSceneTypes: [],
        blockedIntentKeywords: [],
        reviewIntentKeywords: []
      }
    };
    const allowedSceneTypes = new Set((safetyPolicy.admission?.allowedSceneTypes || []).map((item) => normalizeSceneType(item)));
    const blockedSceneTypes = new Set((safetyPolicy.admission?.blockedSceneTypes || []).map((item) => normalizeSceneType(item)));
    const reviewSceneTypes = new Set((safetyPolicy.admission?.reviewSceneTypes || []).map((item) => normalizeSceneType(item)));
    const blockedKeywords = (safetyPolicy.admission?.blockedIntentKeywords || []).map((item) => String(item || "").trim().toLowerCase()).filter(Boolean);
    const reviewKeywords = (safetyPolicy.admission?.reviewIntentKeywords || []).map((item) => String(item || "").trim().toLowerCase()).filter(Boolean);
    const blockedGeneratedFilePatterns = safetyPolicy.admission?.blockedGeneratedFilePatterns || [];
    const reviewGeneratedFilePatterns = safetyPolicy.admission?.reviewGeneratedFilePatterns || [];
    const textSignals = [
      record.userIntent || "",
      ...(record.customizations || []),
      ...(record.generatedFiles || [])
    ].join(" ").toLowerCase();
    const signalCount = [
      isKnownScene,
      hasGeneratedFiles,
      hasSkillSignals,
      hasCustomizations,
      hasOperations,
      hasEntitySignals
    ].filter(Boolean).length;

    if (allowedSceneTypes.size > 0 && !allowedSceneTypes.has(sceneType)) {
      return {
        decision: "skip_policy_filtered",
        score: 0,
        reasons: ["scene-not-in-allowed-list"],
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    if (blockedSceneTypes.has(sceneType)) {
      return {
        decision: "skip_sensitive",
        score: 0,
        reasons: ["blocked-scene-type"],
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    if (blockedKeywords.some((keyword) => textSignals.includes(keyword))) {
      return {
        decision: "skip_sensitive",
        score: 0,
        reasons: ["blocked-intent-keyword"],
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    if (matchesGeneratedFilePattern(record.generatedFiles, blockedGeneratedFilePatterns)) {
      return {
        decision: "skip_sensitive",
        score: 0,
        reasons: ["blocked-generated-file-pattern"],
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    if (!sceneType || config.capture.suppressedSceneTypes.includes(sceneType) || !isProjectRelated) {
      return {
        decision: "skip_irrelevant",
        score: 0,
        reasons: ["not-project-related"],
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    if (incompleteStatuses.has(String(record.taskStatus || "").toLowerCase())) {
      return {
        decision: "skip_incomplete",
        score: 0,
        reasons: ["task-not-completed"],
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    if (config.capture.ignoreDuplicates && (existingFingerprints.has(record.fingerprint) || batchFingerprints.has(record.fingerprint))) {
      return {
        decision: "skip_duplicate",
        score: 0,
        reasons: ["duplicate-session-summary"],
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    const coverage = config.capture.ignoreCoveredBySkill ? evaluateCoverage(record, coverageMetas) : { covered: false, skillName: "" };
    if (coverage.covered) {
      return {
        decision: "skip_already_covered",
        score: 0,
        reasons: ["already-covered-by-project-skill"],
        coveredBySkills: [coverage.skillName].filter(Boolean),
        admissionLevel: "blocked"
      };
    }

    let score = 0;
    if (isKnownScene) {
      score += 2;
      reasons.push("known-scene-type");
    }
    if (hasGeneratedFiles) {
      score += 2;
      reasons.push("has-generated-files");
    }
    if ((record.skillsUsed || []).length >= 2) {
      score += 2;
      reasons.push("multiple-skills-used");
    } else if (hasSkillSignals) {
      score += 1;
      reasons.push("single-skill-used");
    }
    if (hasCustomizations) {
      score += 2;
      reasons.push("has-project-customizations");
    }
    if ((record.entities?.operations || []).length >= 2) {
      score += 1;
      reasons.push("has-reusable-operations");
    }
    if (hasEntitySignals) {
      score += 1;
      reasons.push("has-project-entities");
    }
    if (String(record.userIntent || "").trim().length >= 12) {
      score += 1;
      reasons.push("has-clear-user-intent");
    }

    if (score < config.capture.askScoreThreshold) {
      return {
        decision: "skip_low_value",
        score,
        reasons,
        coveredBySkills: [],
        admissionLevel: "blocked"
      };
    }

    const manualReviewReasons = [];
    const warningReasons = [];
    if (reviewSceneTypes.has(sceneType)) {
      manualReviewReasons.push("review-scene-type");
    }
    if (reviewKeywords.some((keyword) => textSignals.includes(keyword))) {
      manualReviewReasons.push("review-intent-keyword");
    }
    if (matchesGeneratedFilePattern(record.generatedFiles, reviewGeneratedFilePatterns)) {
      manualReviewReasons.push("review-generated-file-pattern");
    }
    if (!hasGeneratedFiles && signalCount <= 2) {
      warningReasons.push("low-signal-capture");
    }

    const admissionLevel = manualReviewReasons.length > 0 ? "review" : "capture";
    reasons.push(...manualReviewReasons, ...warningReasons);

    return {
      decision: "ask_capture",
      score,
      reasons,
      coveredBySkills: [],
      admissionLevel
    };
  }

  function evaluateSessionCapture({ inputPath = "", responsePath = "", stdinText = "", extractMarkedBlock = false, saveExtractedPath = "", toolName = "" } = {}) {
    const paths = ensureConversationRoots();
    const config = readConversationMinerConfig(paths);
    const captureSafetyPolicy = readCaptureSafetyPolicy(paths, context);
    const source = resolveSessionSummarySource({ inputPath, responsePath, stdinText, extractMarkedBlock });
    const rawPayload = source.inputPayload;
    if (saveExtractedPath && source.extraction) writeJson(saveExtractedPath, rawPayload);
    const inputRecords = normalizeInputRecords(rawPayload);
    if (inputRecords.length === 0) throw new Error("Session summary input did not contain any records.");

    const existingRecords = loadConversationRecords().records;
    const existingFingerprints = new Set(existingRecords.map((record) => record.fingerprint || createRecordFingerprint(record)));
    const coverageMetas = loadProjectLocalSkillCoverage(paths);
    const batchFingerprints = new Set();
    const evaluations = [];

    for (const [index, rawRecord] of inputRecords.entries()) {
      const normalizedRecord = normalizeRecord({
        rawRecord,
        projectRoot,
        toolName,
        date: extractDatePart(rawRecord.timestamp || new Date().toISOString()),
        index: index + 1,
        safetyPolicy: captureSafetyPolicy
      });
      const evaluation = evaluateRecordCandidate({
        record: normalizedRecord,
        config: {
          ...config,
          captureSafetyPolicy
        },
        existingFingerprints,
        coverageMetas,
        batchFingerprints
      });
      const captureSafetyGovernance = resolveCaptureSafetyGovernance({
        decision: evaluation.decision,
        admissionLevel: evaluation.admissionLevel,
        reasons: evaluation.reasons
      });
      normalizedRecord.captureAdmissionLevel = evaluation.admissionLevel || "capture";
      normalizedRecord.captureEvaluationDecision = evaluation.decision;
      normalizedRecord.captureEvaluationReasons = evaluation.reasons || [];
      normalizedRecord.captureSafetyGovernanceLevel = captureSafetyGovernance.level;
      normalizedRecord.captureSafetyGovernanceReasons = captureSafetyGovernance.matchedReasons;
      if (evaluation.decision === "ask_capture") batchFingerprints.add(normalizedRecord.fingerprint);
      evaluations.push({
        ...evaluation,
        captureSafetyGovernanceLevel: captureSafetyGovernance.level,
        captureSafetyGovernanceReasons: captureSafetyGovernance.matchedReasons,
        captureSafetyGovernanceSemantics: captureSafetyGovernance.semantics,
        record: normalizedRecord
      });
    }

    const governanceSummary = summarizeCaptureSafetyGovernanceLevels(evaluations);
    const summary = {
      askCapture: evaluations.filter((item) => item.decision === "ask_capture").length,
      askReview: evaluations.filter((item) => item.decision === "ask_capture" && item.admissionLevel === "review").length,
      askWarning: evaluations.filter((item) => item.decision === "ask_capture" && item.captureSafetyGovernanceLevel === "warning").length,
      skipIrrelevant: evaluations.filter((item) => item.decision === "skip_irrelevant").length,
      skipIncomplete: evaluations.filter((item) => item.decision === "skip_incomplete").length,
      skipSensitive: evaluations.filter((item) => item.decision === "skip_sensitive").length,
      skipPolicyFiltered: evaluations.filter((item) => item.decision === "skip_policy_filtered").length,
      skipLowValue: evaluations.filter((item) => item.decision === "skip_low_value").length,
      skipDuplicate: evaluations.filter((item) => item.decision === "skip_duplicate").length,
      skipAlreadyCovered: evaluations.filter((item) => item.decision === "skip_already_covered").length
    };
    const overallDecision = summary.askCapture > 0
      ? "ask_capture"
      : (summary.skipDuplicate > 0
        ? "skip_duplicate"
        : (summary.skipAlreadyCovered > 0
        ? "skip_already_covered"
          : (summary.skipPolicyFiltered > 0
            ? "skip_policy_filtered"
          : (summary.skipSensitive > 0
            ? "skip_sensitive"
            : (summary.skipLowValue > 0 ? "skip_low_value" : (summary.skipIncomplete > 0 ? "skip_incomplete" : "skip_irrelevant"))))));
    const payload = {
      evaluatedAt: new Date().toISOString(),
      source: {
        type: source.sourceType,
        path: source.sourcePath,
        extractedMarkedBlock: Boolean(source.extraction)
      },
      overallDecision,
      config: {
        mode: config.capture.mode,
        askScoreThreshold: config.capture.askScoreThreshold,
        captureSafetyPolicyPath: paths.captureSafetyPolicyPath,
        captureSafetyPolicyEnabled: Boolean(captureSafetyPolicy.enabled),
        allowedSceneTypeCount: (captureSafetyPolicy.admission?.allowedSceneTypes || []).length,
        reviewSceneTypeCount: (captureSafetyPolicy.admission?.reviewSceneTypes || []).length,
        blockedSceneTypeCount: (captureSafetyPolicy.admission?.blockedSceneTypes || []).length,
        reviewKeywordCount: (captureSafetyPolicy.admission?.reviewIntentKeywords || []).length,
        blockedKeywordCount: (captureSafetyPolicy.admission?.blockedIntentKeywords || []).length,
        blockedFilePatternCount: (captureSafetyPolicy.admission?.blockedGeneratedFilePatterns || []).length,
        reviewFilePatternCount: (captureSafetyPolicy.admission?.reviewGeneratedFilePatterns || []).length
      },
      summary,
      reviewBoundary: collectReviewBoundarySummary(evaluations),
      captureSafetyGovernance: {
        semantics: listCaptureSafetyGovernanceSemantics(),
        summary: governanceSummary
      },
      evaluations
    };

    fs.writeFileSync(paths.captureEvaluationReportPath, buildCaptureEvaluationReportMarkdown({ projectName: path.basename(projectRoot), payload }), "utf8");
    return payload;
  }

  function prepareSessionCapture({ inputPath = "", responsePath = "", stdinText = "", extractMarkedBlock = false, saveExtractedPath = "", toolName = "" } = {}) {
    const paths = ensureConversationRoots();
    const evaluation = evaluateSessionCapture({
      inputPath,
      responsePath,
      stdinText,
      extractMarkedBlock,
      saveExtractedPath,
      toolName
    });
    const askCaptureEvaluations = evaluation.evaluations.filter((item) => item.decision === "ask_capture");
    const shouldPrompt = askCaptureEvaluations.length > 0;
    const reviewRequiredEvaluations = askCaptureEvaluations.filter((item) => item.admissionLevel === "review");

    if (!shouldPrompt) {
      return {
        preparedAt: new Date().toISOString(),
        shouldPrompt: false,
        overallDecision: evaluation.overallDecision,
        summary: evaluation.summary,
        reviewBoundary: evaluation.reviewBoundary,
        requestId: "",
        pendingFilePath: "",
        promptMessage: "",
        confirmationCommand: "",
        rejectionCommand: "",
        evaluation
      };
    }

    const requestId = createPendingCaptureId(askCaptureEvaluations.map((item) => item.record));
    const pendingFilePath = path.join(paths.pendingCapturesRoot, `${requestId}.json`);
    const promptMessage = buildPendingCapturePrompt(evaluation.summary);
    const payload = {
      version: "1.1.9",
      requestId,
      status: "pending",
      createdAt: new Date().toISOString(),
      projectName: path.basename(projectRoot),
      promptMessage,
      source: evaluation.source,
      summary: evaluation.summary,
      reviewBoundary: evaluation.reviewBoundary,
      evaluation: {
        overallDecision: evaluation.overallDecision,
        config: evaluation.config
      },
      askCaptureRecords: askCaptureEvaluations.map((item) => item.record),
      reviewRequiredRecords: reviewRequiredEvaluations.map((item) => item.record)
    };
    writeJson(pendingFilePath, payload);

    return {
      preparedAt: payload.createdAt,
      shouldPrompt: true,
      overallDecision: evaluation.overallDecision,
      summary: evaluation.summary,
      reviewBoundary: evaluation.reviewBoundary,
      requestId,
      pendingFilePath,
      promptMessage,
      confirmationCommand: `npx power-ai-skills confirm-session-capture --request ${requestId}`,
      rejectionCommand: `npx power-ai-skills confirm-session-capture --request ${requestId} --reject`,
      evaluation
    };
  }

  return {
    loadConversationRecords,
    evaluateSessionCapture,
    prepareSessionCapture
  };
}
