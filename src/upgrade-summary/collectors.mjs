/**
 * 创建空的包装提升汇总
 */
function createEmptyWrapperPromotionSummary() {
  return {
    total: 0,
    active: 0,
    archived: 0,
    readyForRegistration: 0,
    registeredActive: 0,
    pendingFollowUps: 0,
    draftOrNeedsWork: 0
  };
}

/**
 * 收集包装提升部分
 */
export function collectWrapperPromotionSection(projectRoot, conversationMinerService) {
  const proposalsRoot = path.join(projectRoot, ".power-ai", "proposals");
  if (!fs.existsSync(proposalsRoot)) {
    return {
      available: false,
      reason: "wrapper promotion proposals root is not initialized",
      generatedAt: "",
      summary: createEmptyWrapperPromotionSummary(),
      reportPath: "",
      jsonPath: ""
    };
  }

  const audit = conversationMinerService.generateWrapperPromotionAuditReport({});
  return {
    available: true,
    generatedAt: audit.generatedAt,
    summary: audit.summary,
    reportPath: audit.reportPath,
    jsonPath: audit.jsonPath
  };
}

/**
 * 获取发布清单目录
 */
function getReleaseManifestDir(context) {
  return path.resolve(process.env.POWER_AI_RELEASE_MANIFEST_DIR || path.join(context.packageRoot, "manifest"));
}

/**
 * 收集发布治理部分
 */
export function collectReleaseSection(context, doctorReport) {
  if (doctorReport.mode !== "package-maintenance") {
    return {
      available: false,
      reason: "release summary is only collected in package-maintenance mode"
    };
  }

  const manifestRoot = getReleaseManifestDir(context);
  const versionRecord = readJsonIfExists(path.join(manifestRoot, "version-record.json"));
  const impactReport = readJsonIfExists(path.join(manifestRoot, "impact-report.json"));
  const upgradeRiskReport = readJsonIfExists(
    versionRecord?.artifacts?.upgradeRiskReportPath || path.join(manifestRoot, "upgrade-risk-report.json")
  );
  const promotionTraceReport = readJsonIfExists(
    versionRecord?.artifacts?.promotionTraceReportPath || path.join(manifestRoot, "promotion-trace-report.json")
  );
  const consumerCompatibilityMatrix = readJsonIfExists(
    versionRecord?.artifacts?.consumerCompatibilityMatrixPath || path.join(manifestRoot, "consumer-compatibility-matrix.json")
  );
  const releaseGateReport = readJsonIfExists(
    versionRecord?.artifacts?.releaseGateReportPath || path.join(manifestRoot, "release-gate-report.json")
  );
  const upgradeAdvicePackage = readJsonIfExists(
    versionRecord?.artifacts?.upgradeAdvicePackagePath || path.join(manifestRoot, "upgrade-advice-package.json")
  );
  const automationReport = readJsonIfExists(path.join(manifestRoot, "automation-report.json"));
  const notificationJsonPath = versionRecord?.artifacts?.notificationJsonPath || "";
  const notificationPayload = readJsonIfExists(notificationJsonPath);
  const releaseGroup = (doctorReport.checkGroups || []).find((group) => group.name === "release");

  return {
    available: true,
    ok: releaseGroup?.ok ?? false,
    recordedAt: versionRecord?.recordedAt || "",
    recommendedReleaseLevel: automationReport?.summary?.recommendedReleaseLevel || impactReport?.recommendedReleaseLevel || "",
    overallRiskLevel: pickHigherRiskLevel(
      upgradeRiskReport?.overallRiskLevel || "",
      upgradeAdvicePackage?.overallRiskLevel || "",
      automationReport?.summary?.overallRiskLevel || ""
    ),
    overallReleaseHint: upgradeRiskReport?.overallReleaseHint || upgradeAdvicePackage?.releaseLevel || automationReport?.summary?.recommendedReleaseLevel || "",
    changedFileCount: impactReport?.changedFiles?.length || automationReport?.summary?.changedFileCount || 0,
    affectedDomainCount: impactReport?.affectedDomainCount || automationReport?.summary?.affectedDomainCount || 0,
    affectedSkillCount: impactReport?.affectedSkillCount || automationReport?.summary?.affectedSkillCount || 0,
    impactedOwners: impactReport?.ownerSummary || [],
    followUps: impactReport?.followUps || [],
    risk: upgradeRiskReport ? {
      overallRiskLevel: upgradeRiskReport.overallRiskLevel,
      overallReleaseHint: upgradeRiskReport.overallReleaseHint,
      categoryCount: upgradeRiskReport.summary?.categoryCount || 0,
      categories: (upgradeRiskReport.categories || []).map((category) => ({
        id: category.id,
        title: category.title,
        riskLevel: category.riskLevel,
        matchedFileCount: (category.matchedFiles || []).length
      })),
      recommendedActions: upgradeRiskReport.recommendedActions || [],
      jsonPath: versionRecord?.artifacts?.upgradeRiskReportPath || path.join(manifestRoot, "upgrade-risk-report.json"),
      markdownPath: versionRecord?.artifacts?.upgradeRiskMarkdownPath || path.join(manifestRoot, "upgrade-risk-report.md")
    } : null,
    promotionTrace: promotionTraceReport ? {
      available: Boolean(promotionTraceReport.available),
      matchedRelations: promotionTraceReport.summary?.matchedRelations || 0,
      totalRelations: promotionTraceReport.summary?.totalRelations || 0,
      jsonPath: versionRecord?.artifacts?.promotionTraceReportPath || path.join(manifestRoot, "promotion-trace-report.json"),
      markdownPath: versionRecord?.artifacts?.promotionTraceMarkdownPath || path.join(manifestRoot, "promotion-trace-report.md")
    } : null,
    compatibilityMatrix: consumerCompatibilityMatrix ? {
      scenarioCount: consumerCompatibilityMatrix.summary?.totalScenarios || 0,
      passedScenarioCount: consumerCompatibilityMatrix.summary?.passedScenarios || 0,
      failedScenarioCount: consumerCompatibilityMatrix.summary?.failedScenarios || 0,
      strategies: consumerCompatibilityMatrix.summary?.strategies || [],
      jsonPath: versionRecord?.artifacts?.consumerCompatibilityMatrixPath || path.join(manifestRoot, "consumer-compatibility-matrix.json"),
      markdownPath: versionRecord?.artifacts?.consumerCompatibilityMatrixMarkdownPath || path.join(manifestRoot, "consumer-compatibility-matrix.md")
    } : null,
    releaseGates: releaseGateReport ? {
      overallStatus: releaseGateReport.overallStatus,
      failedGates: releaseGateReport.summary?.failedGates || 0,
      passedGates: releaseGateReport.summary?.passedGates || 0,
      readyForRegistration: releaseGateReport.summary?.readyForRegistration || 0,
      pendingFollowUps: releaseGateReport.summary?.pendingFollowUps || 0,
      stalledProposalCount: releaseGateReport.summary?.stalledProposalCount || 0,
      recommendedActions: releaseGateReport.recommendedActions || [],
      jsonPath: versionRecord?.artifacts?.releaseGateReportPath || path.join(manifestRoot, "release-gate-report.json"),
      markdownPath: versionRecord?.artifacts?.releaseGateMarkdownPath || path.join(manifestRoot, "release-gate-report.md")
    } : null,
    upgradeAdvice: upgradeAdvicePackage ? {
      consumerCommandCount: upgradeAdvicePackage.summary?.consumerCommandCount || 0,
      maintainerCommandCount: upgradeAdvicePackage.summary?.maintainerCommandCount || 0,
      manualCheckCount: upgradeAdvicePackage.summary?.manualCheckCount || 0,
      blockingCheckCount: upgradeAdvicePackage.summary?.blockingCheckCount || 0,
      blocked: Boolean(upgradeAdvicePackage.blocked),
      jsonPath: versionRecord?.artifacts?.upgradeAdvicePackagePath || path.join(manifestRoot, "upgrade-advice-package.json"),
      markdownPath: versionRecord?.artifacts?.upgradeAdvicePackageMarkdownPath || path.join(manifestRoot, "upgrade-advice-package.md")
    } : null,
    consumerVerification: automationReport?.consumerVerification || { skipped: true, reason: "not available" },
    notification: notificationPayload ? {
      title: notificationPayload.title || "",
      level: notificationPayload.level || "",
      jsonPath: notificationJsonPath,
      markdownPath: versionRecord?.artifacts?.notificationMarkdownPath || ""
    } : null,
    artifacts: {
      manifestRoot,
      versionRecordPath: path.join(manifestRoot, "version-record.json"),
      impactReportPath: path.join(manifestRoot, "impact-report.json"),
      upgradeRiskReportPath: versionRecord?.artifacts?.upgradeRiskReportPath || path.join(manifestRoot, "upgrade-risk-report.json"),
      promotionTraceReportPath: versionRecord?.artifacts?.promotionTraceReportPath || path.join(manifestRoot, "promotion-trace-report.json"),
      consumerCompatibilityMatrixPath: versionRecord?.artifacts?.consumerCompatibilityMatrixPath || path.join(manifestRoot, "consumer-compatibility-matrix.json"),
      automationReportPath: path.join(manifestRoot, "automation-report.json"),
      doctorReportPath: doctorReport.reportPath,
      doctorJsonPath: doctorReport.jsonPath
    }
  };
}
