import fs from "node:fs";
import path from "node:path";
import { readJson } from "../shared/fs.mjs";

/**
 * 读取 JSON 文件（如果存在）
 */
function readJsonIfExists(filePath) {
  return filePath && fs.existsSync(filePath) ? readJson(filePath) : null;
}

function pickHigherRiskLevel(...levels) {
  const weights = { low: 1, medium: 2, high: 3 };
  return levels
    .filter((level) => typeof level === "string" && weights[level])
    .sort((left, right) => weights[right] - weights[left])[0] || "";
}

/**
 * 统计项目级技能目录数量
 */
function countProjectLocalSkillDirectories(rootDir) {
  if (!rootDir || !fs.existsSync(rootDir)) return 0;
  return fs.readdirSync(rootDir, { withFileTypes: true }).filter((entry) => entry.isDirectory()).length;
}

/**
 * 收集项目扫描部分
 */
export function collectProjectScanSection(projectRoot, projectScanService) {
  try {
    const artifacts = projectScanService.loadAnalysisArtifacts();
    const skillsRoot = path.join(projectRoot, ".power-ai", "skills", "project-local");
    const autoGeneratedRoot = path.join(skillsRoot, "auto-generated");
    const manualRoot = path.join(skillsRoot, "manual");
    return {
      available: true,
      generatedAt: artifacts.patterns.generatedAt,
      projectName: artifacts.projectProfile.projectName,
      viewFileCount: artifacts.projectProfile.fileSummary?.viewFileCount || 0,
      patternCount: (artifacts.patterns.patterns || []).length,
      reviewSummary: artifacts.patternReview.summary,
      feedbackSummary: artifacts.patternReview.feedbackSummary || {
        overrides: artifacts.patternFeedback?.summary?.total || 0,
        applied: 0,
        stale: 0
      },
      diffSummary: artifacts.patternDiff.summary,
      autoGeneratedSkillCount: countProjectLocalSkillDirectories(autoGeneratedRoot),
      manualSkillCount: countProjectLocalSkillDirectories(manualRoot),
      artifacts: {
        patternsPath: projectScanService.getAnalysisPaths().patternsPath,
        patternReviewPath: projectScanService.getAnalysisPaths().patternReviewPath,
        patternFeedbackPath: projectScanService.getAnalysisPaths().patternFeedbackPath,
        summaryReportPath: projectScanService.getAnalysisPaths().summaryReportPath,
        feedbackReportPath: projectScanService.getAnalysisPaths().feedbackReportPath
      }
    };
  } catch (error) {
    return {
      available: false,
      reason: error.message
    };
  }
}

